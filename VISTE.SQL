DROP VIEW ESTRATTO_CRYPTO;

/*
    VISUALIZZA TUTTI GLI SCAMBI CON ANNESSO EXCHANGE UTILIZZATO PER EFFETTUARLO
*/
CREATE OR REPLACE VIEW ESTRATTO_CRYPTO AS
    SELECT NOME_EXCHANGE,BLOCKCHAIN,VALUTA_OTTENUTA,QUANTITA_OTTENUTA,VALUTA_SCAMBIATA,QUANTITA_SCAMBIATA,DATA_S
        FROM EXCHANGE_DECENTRALIZZATO DEX JOIN SCAMBIO S ON DEX.NOME_EXCHANGE = S.NOME_EXCHANGE_DEX
            ORDER BY DATA_S ASC;