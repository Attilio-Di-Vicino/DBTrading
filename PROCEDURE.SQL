/*
    2- ELIMINARE DAL DATABASE TUTTE LE CARTE SCADUTE
*/
CREATE OR REPLACE PROCEDURE ELIMINARE_CARTE_SCADUTE
IS
BEGIN
    FOR i IN
        (
            SELECT NUMERO_CONTO
            FROM CONTO_CORRENTE
            WHERE DATA_DI_SCADENZA > SYSDATE
        )
    LOOP
        DELETE FROM CONTO_CORRENTE WHERE NUMERO_CONTO = i.NUMERO_CONTO;
    END LOOP;
END ELIMINARE_CARTE_SCADUTE;

